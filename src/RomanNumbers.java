    public static int romanToArab (String str) {
        int c = -1;
        for (int i = 0; i < 100; i++) {
            if (ROMAN_NUMBERS[i].equals(str)) {
                c = i;
            }
        }
        return c;
    }

    public static StringBuilder arabToRoman (int arab) {
        StringBuilder arabNumber = new StringBuilder();
        for (int i = 0; i < 100; i++) {
            if (i == arab) {
                arabNumber.append(ROMAN_NUMBERS[i]);
            }
        }
        return arabNumber;
    }
